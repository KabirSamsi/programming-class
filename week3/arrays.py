# Example of making an array in Python

array = [1, 2, 3, 4, 5]

# to read a value from an array (also called list commonly), you specify an index (position in array)
# the first index is 0

print(array[0])  # should be 1
print(array[2])  # should be 3

# to add a value to an array, we use the .append() method

array.append(6) # add the value 6 to the end of the array
print()